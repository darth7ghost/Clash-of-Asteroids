    int x=0;
    private void update(){
        x++;
    }
    
    private void draw(){
        bs = canvas.getBufferStrategy();
        if(bs == null){
            canvas.createBufferStrategy(3);
            return;
        }
        g = bs.getDrawGraphics();
        //-------------------------------------------
            g.clearRect(0, 0, WIDTH, HEIGHT);
            g.drawString(""+AVERAGEFPS, 100, 100);
            g.setColor(Color.black);
        //-------------------------------------------
        g.dispose();
        bs.show();
    }
    
    @Override
    public void run() {
        long now = 0;
        long lastTime = System.nanoTime();
        int frames = 0;
        long time = 0;
        while(running){
            now = System.nanoTime();
            delta += (now - lastTime)/TARGETTIME;
            time += (now - lastTime);
            lastTime = now;
            if(delta>=1){
                update();
                draw();
                delta--;
                frames++;
            }
            if(time >= 1000000000){
                AVERAGEFPS = frames;
                frames = 0;
                time = 0;
            }
        }
        stop();
    }
    
    private void start(){
        thread = new Thread(this);
        thread.start();
        running = true;
    }
    
    private void stop(){
        try {
            thread.join();
            running = false;
            } catch (InterruptedException ex) {
            Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
